    private void btnSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortActionPerformed
        //Filter

        //Collect the sort parameters (filter)
        gameSortSelected = (String)cmbGame.getSelectedItem();
        platformSortSelected = (String)cmbPlatform.getSelectedItem();
        usernameSort = txfUsername.getText();
        
        //Set the variables to null if the default item is selected (therefore, don't want to sort by that category)
        if(!gameSortSelected.equals("All Games"))
            gameSort = gameSortSelected;
        else
            gameSort = "";
        
        if(!platformSortSelected.equals("Any Platform"))
            platformSort = platformSortSelected;
        else
            platformSort = "";
        

        //Send the filter parameters (and userID because we don't want to lose it) to a new Main class and dispose of this one
        new Main(uID, gameSort, platformSort, usernameSort).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnSortActionPerformed

