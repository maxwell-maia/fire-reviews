    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        //Delete a game from the database AND delete all reviews made about that game
        
        String gameToDelete = (String)cmbGameDelete.getSelectedItem();
        int gameToDeleteID = 1000000000; //initialise integer in such a way that if the ID is not collected properly, an SQL error will occur, preventing the wrong game and reviews being deleted
        
        for(int i = 0; i<fullSize; i++) //loop through game array to find the GameID of selected game
        {
            if(fullArray[i].getName().equals(gameToDelete))
            {
                gameToDeleteID = fullArray[i].getNumber(); //get the game id
            }
        }
        
        //use SQLSTastements object to delete the game and reviews including the game
        SQLSTatements dd = new SQLSTatements();
        dd.deleteGame(gameToDelete, gameToDeleteID);
    }//GEN-LAST:event_btnDeleteActionPerformed

